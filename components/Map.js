import React, {useContext} from 'react';
import MapView, { Marker } from 'react-native-maps';
import { StyleSheet, Dimensions, Button, SafeAreaView } from 'react-native';
import { StatusBar } from 'expo-status-bar';
import {DataContext} from './Context'


export default function Map({ navigation }) {
  // useContext reads the context and subscribes to its changes
  const {places, region} = useContext(DataContext)
  console.log(places)

  // returning the map and markers for each venue
  return (
    <SafeAreaView style={styles.container}>
      <Button
        color='#3b3a3a'
        title="Go to List view"
        onPress={() => navigation.navigate('Home')}
      />
      <MapView 
      style={styles.map} 
      region={region}
      showsUserLocation
      showsMyLocationButton
      customMapStyle={mapStyle}
      >
     {places.map((place, index) => (
        <Marker 
          key={index} 
          title={place.name} 
          description={place.type} 
          coordinate={place.coords} 
          pinColor='green' />
      ))}
   
    </MapView>
     <StatusBar style="auto" />
    </SafeAreaView>
  );
}

